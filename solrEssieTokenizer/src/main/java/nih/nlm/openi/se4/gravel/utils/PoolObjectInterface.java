package gravel.utils;

//--------------------------------------------------------------------------------------------------------
// PoolObjectInterface
//--------------------------------------------------------------------------------------------------------

public interface PoolObjectInterface extends Constants {

//--------------------------------------------------------------------------------------------------------
// canRecycle
//
// Called when object put back into pool
// Return false to indicate object not allowed back in pool
// Objects that fail the check() below, should return false
//--------------------------------------------------------------------------------------------------------

  public boolean canRecycle();

//--------------------------------------------------------------------------------------------------------
// open
//
// Allocate whatever resources needed
//--------------------------------------------------------------------------------------------------------

  public void open() throws Exception;

//--------------------------------------------------------------------------------------------------------
// close
//
// Release whatever resources allocated
//--------------------------------------------------------------------------------------------------------

  public void close();

//--------------------------------------------------------------------------------------------------------
// check
//
// Called in background by cleanPool() to see if object still valid
// Typically runs some test and returns false if test fails
// Once object is known to be invalid, it should return false from canRecycle()
//--------------------------------------------------------------------------------------------------------

  public boolean check();

}

