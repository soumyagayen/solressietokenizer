package gravel.store.hash;

import gravel.store.data.*;
import gravel.store.var.*;

//--------------------------------------------------------------------------------------------------------
// DataHashStore
//--------------------------------------------------------------------------------------------------------

public abstract class DataHashStore extends HashStore {

//--------------------------------------------------------------------------------------------------------
// DataHashStore 
//--------------------------------------------------------------------------------------------------------

  protected DataHashStore(DataStore inKeyStore, VarStore inHashStore, VarStore inLookupStore, 
      VarStore inPtrStore) {
    super(inKeyStore,inHashStore,inLookupStore,inPtrStore); }

//--------------------------------------------------------------------------------------------------------
// deleteStore
//--------------------------------------------------------------------------------------------------------

  public static void deleteStore(String inFilename) {
    DataStore.deleteStore(inFilename); 
    HashStore.deleteStore(inFilename); 
  }

//--------------------------------------------------------------------------------------------------------
// getKeyStore
//--------------------------------------------------------------------------------------------------------

  protected DataStore getKeyStore() { return (DataStore) super.getKeyStore(); }

//--------------------------------------------------------------------------------------------------------
// dangerousGetKeyStore
//--------------------------------------------------------------------------------------------------------

  // unprotected access to inner store - easy to screw up
  public DataStore dangerousGetKeyStore() { return (DataStore) super.dangerousGetKeyStore(); }

//--------------------------------------------------------------------------------------------------------
// getDataSize
//--------------------------------------------------------------------------------------------------------

  public long getDataSize() { return getKeyStore().getDataSize(); }

//--------------------------------------------------------------------------------------------------------
// getDataCapacity
//--------------------------------------------------------------------------------------------------------

  public long getDataCapacity() { return getKeyStore().getDataCapacity(); }

//--------------------------------------------------------------------------------------------------------
// getOffset
//--------------------------------------------------------------------------------------------------------

  public long getOffset(long inIndex) { return getKeyStore().getOffset(inIndex); }

}

